            <h2>Herausforderung</h2>

            <p>Die Corona-Krise trifft kleine Unternehmen ohne große Finanzkraft am stärksten. Vom Einzelhandel bis zum Gastronom; vom Handwerksbetrieb bis zum Friseursalon - laut einer Umfrage sehen 77% aller Betribe ihre wirtschaftliche Existenz gefährdet. Um Entlassungen zu vermeiden, unterstützt der Staat Betriebe in Kurzarbeit finanziell. </p>

            <p>In der Praxis gibt es hier jedoch ein Problem: Der Weg zum Kurzarbeitergeld ist kompliziert und verworren, und Antragsformulare sind ohne juristisches Know-How oft schwer verständlich. Zusätzlich sind viele Unternehmer von der Vielzahl an verstreuten Informationen überfordert, wie persönliche Erfahrungen von Projektmitgliedern und Gespräche mit verschiedenen Kleinstunternehmern zeigen.</p>

            <p>Das wollen wir ändern! Unser Ziel: Den Weg zum Kurzarbeitergeld ganz einfach machen. </p>

            <p>Unsere Lösung wurde für alle kleinen Betriebe mit weniger als 50 Mitarbeitern entwickelt: Vom Handwerker bis zur Friseurin, vom Gastronom bis zum Laden um die Ecke - für insgesamt 2,2 Millionen betroffene Unternehmen in Deutschland, mit über 11 Millionen Beschäftigten! </p>

            <p>Damit kleine und mittlere Unternehmen auch in Zukunft weiter das Rückgrat der deutschen Wirtschaft bilden können und Arbeitnehmer auch nach der Krise noch einen Job haben! </p>

            <h2>Unsere Lösung</h2>

            <p>Auf kurzarbeit-einfach.de beantworten wir die wesentlichen Fragen zur Kurzarbeit, die Kleinunternehmer jetzt umtreiben. Und alles in verständlicher Sprache: </p>

            <ul>
                <li><em>Was ist eigentlich Kurzarbeitergeld?</em> </li>
                <li><em>Kann ich es für mein Unternehmen bekommen?</em> </li>
                <li><em>Und was muss ich dafür machen?</em> </li>
            </ul>

            <p>Das alles übernimmt U:DO, unser neu entwickelter Chatbot. Am 20.03.2020 geboren, stellt er dir ganz einfache Fragen, überprüft alle Eingaben auf eventuelle Unvereinbarkeit mit dem Kurzarbeitergeld.
            Danach füllt er das Formular zur Anzeige von Kurzarbeit für die Bundesagentur für Arbeit für dich aus und sagt dir, was du mit diesem Formular machen musst.</p>

            <p>Damit mindert unser Prototyp schon heute die Einstiegshürden zur Beantragung von Kurzarbeit. Wir schaffen Transparenz über die Voraussetzungen und den Prozess und unterstützen Unternehmer bei den ersten Schritten, nämlich der Anzeige von Kurzarbeit bei der Arbeitsagentur. In Zukunft wollen wir hier auch weitere Prozessschritte (wie z.B. die Beantragung der Erstattung von Kurzarbeitergeld) digital vereinfachen. </p>

            <p>Zusätzlich ist unser Code OpenSource (siehe Github-Link unten), damit auch andere Entwickler darauf aufbauen und weitere komplexe bürokratische Prozesse durch Digitalisierung vereinfachen können.</p>

